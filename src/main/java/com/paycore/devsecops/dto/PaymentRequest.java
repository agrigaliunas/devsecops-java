package com.paycore.devsecops.dto;


import java.math.BigDecimal;

public class PaymentRequest {

    private String companyId;      // Identificación B2B
    private String payerName;      // PII
    private String payerEmail;     // PII
    private String cuitCuil;       // PII
    private String cbu;            // Sensitive Financial Data
    private String cardToken;      // Token de tarjeta (SFD)
    private BigDecimal amount;     // Monto
    private String currency;       // ARS, USD, etc.
    private String description;    // Detalle del pago

    // Getters y setters (podés generar con tu IDE)

    public String getCompanyId() {
        return companyId;
    }

    public void setCompanyId(String companyId) {
        this.companyId = companyId;
    }

    public String getPayerName() {
        return payerName;
    }

    public void setPayerName(String payerName) {
        this.payerName = payerName;
    }

    public String getPayerEmail() {
        return payerEmail;
    }

    public void setPayerEmail(String payerEmail) {
        this.payerEmail = payerEmail;
    }

    public String getCuitCuil() {
        return cuitCuil;
    }

    public void setCuitCuil(String cuitCuil) {
        this.cuitCuil = cuitCuil;
    }

    public String getCbu() {
        return cbu;
    }

    public void setCbu(String cbu) {
        this.cbu = cbu;
    }

    public String getCardToken() {
        return cardToken;
    }

    public void setCardToken(String cardToken) {
        this.cardToken = cardToken;
    }

    public BigDecimal getAmount() {
        return amount;
    }

    public void setAmount(BigDecimal amount) {
        this.amount = amount;
    }

    public String getCurrency() {
        return currency;
    }

    public void setCurrency(String currency) {
        this.currency = currency;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }
}